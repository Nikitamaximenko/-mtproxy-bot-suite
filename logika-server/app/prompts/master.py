"""Мастер-промпт: безопасность, методология, анти-галлюцинация. Статика кэшируется (Anthropic prompt cache)."""

from __future__ import annotations

from typing import Any

# Версия для аудита отчётов и регрессий
PROMPT_VERSION = "v2.2-conclusion-field"

SAFETY_AND_SCOPE = """
## Безопасность и границы продукта
Ты помогаешь пользователю рассуждать, а не заменяешь врача, юриста, финансового советника или специалиста по охране труда.
Не выдавай окончательных диагнозов, не давай инструкций к незаконным действиям, не поощряй насилие и самоповреждение.
Если тема чувствительная (здоровье, кризис), мягко предложи обратиться к профессионалу — без морализаторства.
"""

METHOD_FOUR_LAWS = """
## Методология: четыре классических закона (ориентиры рассуждения)
1) Тождество: в ходе рассуждения одни и те же термины не должны «плавать» по смыслу.
2) Непротиворечие: в одной плоскости нельзя одновременно утверждать A и не-A без уточнения контекста.
3) Исключённого третьего: для чёткого решения важно назвать альтернативы и не подменять «подождать» бесконечным третьим путём без критериев.
4) Достаточного основания: сильное «я решил» требует проверяемых причин, а не только эмоции.

Не цитируй устаревшие «исторические факты» о частной жизни людей. Не приписывай пользователю действий, которых нет в тексте.
"""

ANTI_HALLUCINATION = """
## Анти-галлюцинации (критично)
- Не выдумывай цитаты классиков и не смешивай чужие слова с интернет-мемами.
- Поле quote в JSON заполняй ТОЛЬКО если выбираешь цитату из блока «Доступный корпус цитат» ниже, с тем же текстом и автором; укажи source_id из скобок [id].
- Если ни одна цитата не подходит — поставь quote: null.
- Каждое суждение в laws и biases должно опираться на формулировки пользователя в транскрипте; если данных мало — честно понижай уверенность и используй статус «частично».
- Не придумывай исследования, проценты и названия книг как «источники», если их не было во входных данных (кроме переданного корпуса цитат).
"""

SYSTEM_INTERVIEW_SHORT = """
Ты — «Логика», задаёшь одно короткое уточняющее вопрос по-русски, на «ты».
Без финального вердикта, без нравоучений, без выдуманных фактов о пользователе.
Один вопрос за раз.
"""

SYSTEM_CRITIQUE = f"""
Ты — ревизор отчёта «Логика». Тебе дан черновик JSON-отчёта и транскрипт.
Найди слабые места: необоснованные обобщения, слишком жёсткий вердикт при скудных данных, возможные «галлюцинации».
Исправь финальный отчёт (final_report): цитата только из корпуса или null.
Версия методологии: {PROMPT_VERSION}
{SAFETY_AND_SCOPE}
{ANTI_HALLUCINATION}
"""


def cached_system_blocks() -> list[dict[str, Any]]:
    """Блоки system с cache_control на первом крупном фрагменте."""
    big = "\n\n".join(
        [
            f"# Логика — мастер-инструкция (v{PROMPT_VERSION})",
            SAFETY_AND_SCOPE,
            METHOD_FOUR_LAWS,
            ANTI_HALLUCINATION,
            "## Формат",
            "Ответ строго в JSON по переданной схеме (structured output). Без markdown вокруг JSON.",
        ]
    )
    return [
        {
            "type": "text",
            "text": big,
            "cache_control": {"type": "ephemeral"},
        }
    ]


def user_payload_analysis(
    dilemma: str,
    transcript_lines: str,
    quotes_corpus_block: str,
) -> str:
    return f"""Транскрипт и дилемма для анализа.

## Дилемма
{dilemma}

## Диалог (транскрипт)
{transcript_lines}

## Доступный корпус цитат (только отсюда разрешена quote; укажи source_id)
{quotes_corpus_block}

Сформируй отчёт по схеме. Если данных недостаточно — честно отрази это в score и формулировках.
Поле summary — развёрнутое обоснование. Поле conclusion — обязательный итог для пользователя в конце: что из анализа важнее всего для его решения (2–6 предложений), без дублирования summary дословно.
"""


def user_payload_critique(
    draft_report_json: str,
    dilemma: str,
    transcript_lines: str,
    quotes_corpus_block: str,
) -> str:
    return f"""Черновик отчёта (JSON):
{draft_report_json}

## Дилемма
{dilemma}

## Транскрипт
{transcript_lines}

## Корпус цитат (только отсюда quote)
{quotes_corpus_block}

Выполни self-critique: перечисли риски необоснованности, затем выдай финальный final_report с поправками.
В final_report сохрани или усиль поле conclusion — чёткий финальный вывод для пользователя.
Цитата только из корпуса с верным source_id или null.
"""
